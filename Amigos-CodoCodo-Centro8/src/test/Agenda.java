    private void cargar(){
        List<Contacto>lista=new ArrayList();
        fileText.getLines().forEach(item->{
            String[]fila=item.split(",");
            lista.add(new Contacto(fila[0],fila[1],fila[2],fila[3]));
        });
        new Table<Contacto>().cargar(tblContactos, lista);
    }
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // Evento agregar
        String registro=txtNombre.getText()+","+txtApellido.getText()+","
                +txtTelefono.getText()+","+txtEmail.getText()+"\n";
        fileText.appendText(registro);
        txtNombre.setText("");
        txtApellido.setText("");
        txtTelefono.setText("");
        txtEmail.setText("");
        txtNombre.requestFocus();
        cargar();
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
        // Evento buscar
        List<Contacto>lista=new ArrayList();
        fileText.getLines(txtBuscar.getText()).forEach(item->{
            String[]fila=item.split(",");
            lista.add(new Contacto(fila[0],fila[1],fila[2],fila[3]));
        });
        new Table<Contacto>().cargar(tblContactos, lista);
    }//GEN-LAST:event_txtBuscarKeyReleased
